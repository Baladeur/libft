#include "libft.h"

void	ft_putint(long long int n)
{
	if (n == -9223372036854775807)
		ft_putstr("-9223372036854775807");
	else if (n < 0)
	{
		ft_putchar('-');
		ft_putint(-1 * n);
	}
	else if (n > 9)
	{
		ft_putint(n / 10);
		ft_putchar(n % 10 + 48);
	}
	else
		ft_putchar(n % 10 + 48);
}
